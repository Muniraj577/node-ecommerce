let mongoose = require('mongoose');
let productSchema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    slug: {
        type: String
    },
    desc: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    image: {
        type: String,
    }
});

let Product = module.exports = mongoose.model('Product', productSchema);